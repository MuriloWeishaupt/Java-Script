//Métodos Numéricos:

//O Objeto Number,pai dos números, contém métodos muito úteis para trabalhar com números em JS.
//A maioria dos tipos de dados também tem um objeto pai, com: String, Object e Array.



//Parsefloat:

console.log(parseFloat('12.999'))
console.log(Number.parseFloat('12.999'))

//ParseInt:

console.log(parseInt('10'))
console.log(parseInt('14.252'))

//Tofixed:

console.log(26.23545363.toFixed(1))

//IsNaN:
console.log(isNaN('teste'))
console.log(isNaN(32))
console.log(isNaN('32')) //Converte string para Number

//MAX_VALUE e MIN_VALUE:

console.log(Number.MAX_VALUE)
console.log(Number.MIN_VALUE)

