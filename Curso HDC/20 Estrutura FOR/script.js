// Estrutura FOR:

//O FOR tem uma sintaxe mais complexa, mas é optado peka maioria dos programadores.
//Apesar de parecer mais difícil, como a instrução fica toda em uma linha proporciona maior controle.


for(let i = 0; i < 10; i++ ) {
   console.log(i)
}

let arr = [1, 2, 3, 4, 5]

for(j = 0; j < arr.length; j++) {
   console.log(arr[j])
}

for(c = 5; c <= 100; c+= 5) {
   console.log(c)
}