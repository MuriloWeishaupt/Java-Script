// Estrutura de repetição com Teste no começo
var c = 1
while (c <= 10) {
    console.log(`Passo ${c}`)
    c++
}

// Estrutura de repetição com Teste no Final
do {
    console.log (`Passo ${c}`)
} while (c <= 10)

// Esrutura de repetição com Teste e complemento
for (var c = 1; c <= 10; c++) {
    console.log (`Passo ${c}`)
}